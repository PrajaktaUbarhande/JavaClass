package Loops;

public class SwitchCaseDemo {
	
	public static void main(String[] args) {

		String day="Monday";
		
		switch(day)
		{
			case  "Monday"  :
							 	System.out.println("It is monday");
							 	break;
							 	
			case  "Tuesday" :
				 				System.out.println("It is Tuesday");
				 				break;
				 				
	 				
			case  "Wensday" :
 								System.out.println("It is Wensday");
 								break;
 								
 			default:
 								System.out.println("Wrong Day");
 								break;

							 
}//switch ends
		
}
}// class end
